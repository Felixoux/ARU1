                <h2 class="pt-2 uppercase">reglement d'ordre <span>interieur</span></h2>
                <h4 class="uppercase">preliminaire</h4>
                <p>Pour que l'école puisse accomplir sa mission d'enseignement et assurer à tous des chances égales d'accéder à la
                citoyenneté sans être confronté à l'insulte, à la peur, aux conflits ou à toute violence verbale ou physique, il faut
                que des règles claires codifient le comportement de tous et que des sanctions soient fixées pour tout manquement à
                ces règles.</p>
                <h4>Art. 1 – Mise en application</h4>
                <p>Le présent règlement d'ordre intérieur est pris en application des textes suivants qu’il complète et précise (ces
                documents sont remis lors de l’inscription et doivent être conservés pendant toute la durée de la scolarité dans
                l’établissement): <br>
                - Projet éducatif et projet pédagogique de l’enseignement organisé par la Communauté française, <br>
                - Règlement des études de l’enseignement secondaire organisé par la Communauté française, <br>
                - Règlement d’ordre intérieur des établissements d’enseignement secondaire de la Communauté française, <br>
                - Projet pédagogique de l’Athénée Royal d’Uccle 1. <br>
                Il ne dispense pas les élèves et leurs parents, s’ils sont mineurs, de se conformer aux textes légaux, règlements et
                instructions administratives diffusés par le Ministère de la Communauté française, ainsi qu’à toute note interne ou
                recommandation émanant de l’établissement.</p>
                <h4>Art. 2 - Du comportement général.</h4>
                <p>
                - Les élèves auront une tenue et un comportement corrects en toutes circonstances tant à l'intérieur qu'à
                l'extérieur de l'établissement. <br>
                - Tout acte de violence est évidemment proscrit. <br>
                - Aucun couvre-chef ne sera toléré à l’intérieur des bâtiments. <br>
                - Les élèves ne peuvent se présenter aux cours en buvant ou en mangeant <br>
                - Sont interdits dans l’établissement sous peine d’une confiscation pouvant aller jusqu’à la fin de l’année
                scolaire: <br>
                - l’usage de tout appareil, jeu ou objet susceptible de troubler le travail ou la discipline (GSM, baladeur, MP3...).
                Si des élèves en apportent, l’école décline toute responsabilité en cas de perte ou de vol et n’accepte donc, en
                aucun cas, d’en assurer la garde (par exemple pendant les cours d’éducation physique). Il en va de même
                pour tout objet étranger aux cours. <br>
                - la détention de ceux qui risquent de mettre en danger la sécurité des personnes ou des biens (skate-board, patins,
                balles dures en dehors du cours de gymnastique,...)
                Dans le cas spécifique des GSM, tout appareil trouvé ou confisqué ne sera remis que sur présentation de la
                facture d’achat. <br>
                - Aucune initiative collective ou individuelle sortant du cadre normal des activités scolaires ne peut être prise sans
                avoir reçu l’accord préalable du Chef d’établissement (affichages, pétitions, ...) <br>
                - Tout usage abusif du système d’alarme sera sanctionné.
                </p>
                <h4 class="uppercase">prevention des assuetudes</h4>
                <p>- L’usage du tabac est interdit en tous lieux dans les bâtiments, en ce compris les cours, le préau et les abords
                immédiats de l'établissement, sous peine de sanction immédiate de retenue qui peut s’accompagner de travaux
                d’intérêt général (nettoyage,...). <br>
                - La consommation de toute substance, licite ou non (alcool, cannabis, ...), susceptible de modifier le
                comportement de façon telle qu’elle perturbe la relation pédagogique est strictement interdite du début à la
                fin de la journée scolaire, en ce compris les récréations et la pause de midi. <br>
                - Toute détention de ces substances dans l’école est également prohibée.</p>
                <h4 class="uppercase">vandalisme</h4>
                <p>Toute dégradation volontaire du matériel, du bâtiment ou du cadre de vie sera réparée par le responsable ou à ses
                frais et sanctionnée.</p>
                <h4>Art. 3 - De l’accès aux bâtiments et des consignes de sécurité</h4>
                <p>Les élèves seront présents dans la cour dès la 1ère sonnerie. <br>
                Ils entreront le matin et au début de l’après-midi par la grande grille. <br>
                Ils sortiront par la grande grille à 12h35 et 15h50. <br>
                Pendant les heures de fermeture de cette grille (de 8h10 à 12h35, de 12h45 à 13h00, de 13h20 à 15h50 et après
                16h), ils passeront par la porte principale. <br>
                Les cyclistes et motocyclistes ne peuvent gagner ou quitter le garage mis à leur disposition qu'en roulant au pas: <br>
                ils laisseront la priorité aux piétons, élèves, parents et passants et veilleront à ne jamais mettre leur sécurité en
                danger. <br>
                Ils quitteront le garage immédiatement après avoir remisé leur engin. <br>
                N.B. Le garage à vélos est une facilité offerte aux élèves, il n'y sera organisé aucune surveillance ce qui, en cas de
                vol ou de détérioration, exclut toute responsabilité de l'école, et donc de la Communauté française de Belgique.</p>
                <h4>Art. 4 - Du déplacement des élèves dans les bâtiments</h4>
                <p>- Au début des cours (le matin, l'après-midi et après la récréation), les élèves se rangent dans la cour et attendent
                leur professeur. <br>
                - Au changement de cours, les élèves gagnent le nouveau local sans s'attarder dans les couloirs, la cour ou le
                préau. <br>
                - A la fin des cours, les élèves quittent immédiatement l'établissement. Ils ont l’obligation de rentrer directement
                chez eux. <br>
                - Les élèves ne stationnent pas aux abords de l’école.</p>
                <h4>Art. 5 – Des retards.</h4>
                <p>
                - Tout élève en retard ne peut se présenter en classe sans avoir fait viser son journal de classe à l’entrée ou au
                local photocopie (ou à l'étude si ce dernier est fermé). Le professeur appréciera l'opportunité de l’admettre au cours
                ou de le renvoyer à l'étude avec une note au journal de classe. <br>
                - Tout retard de plus d’une demi-heure est considéré comme une heure d’absence.
                </p>
                <h4>Art. 6 – Des licenciements.</h4>
                <p>- Les élèves peuvent être licenciés conformément aux autorisations données par leurs parents en début d'année
                scolaire. <br>
                - Les licenciements sont indiqués au journal de classe et visés par les parents des élèves mineurs. <br>
                - Les élèves inscrits au repas ne peuvent être licenciés en fin de matinée ou début d'après-midi sauf dans des
                circonstances exceptionnelles justifiées par un mot des parents.</p>
                <h4>Art. 7 – Des absences</h4>
                <h5>Fréquentation scolaire.</h5>
                <p> 
                Aucune absence n'est tolérée si elle n'est pas dûment motivée et appuyée de pièces justificatives fournies dans
                les délais conformément aux art.15 à 29 du Règlement d’ordre intérieur des établissements d’enseignement
                secondaire de la Communauté française (voir art. 1).
                Sont principalement considérées comme justifiées les absences motivées:
                l° par l'indisposition ou la maladie de l'élève couverte par certificat médical ou document officiel remis par un
                centre hospitalier,
                2° par le décès d’un proche.
                3° par des cas de force majeure ou des circonstances exceptionnelles appréciés par le chef d’établissement.
                3
                Les délais de remise des justificatifs sont:
                - le lendemain du dernier jour d'absence lorsque celle-ci ne dépasse pas trois jours
                - le 4e jour d'absence dans tous les autres cas.
                Dans le « Règlement des études», il faut entendre par « épreuves d’évaluation sommative » (pour lesquelles un
                certificat médical est exigé en cas d’absence) non seulement les examens, mais aussi les interrogations de
                synthèse prévues en cours d’année.
                </p> 
                <h5>Dispositions propres à l’A.R.U. 1:</h5>
                <p>
                - Les parents ou l'élève majeur peuvent motiver 10 demi-journées d'absence au cours de l'année scolaire;
                cependant, l'appréciation de la légitimité de la justification produite est de la compétence et de la responsabilité
                du chef d'établissement et non des parents ou de l'élève majeur.
                - Est considérée comme demi-journée d'absence injustifiée, l'absence non justifiée de l'élève à
                1 période de cours ou à 1 heure d’étude organisée en remplacement d’un cours.
                - La présence aux repas et aux autres heures d’étude est contrôlée comme celle aux cours.
                - Les élèves ne peuvent quitter l'école pendant les heures de présence obligatoire, pour quelque raison que ce
                soit, sans l'autorisation de la Direction et de leurs parents s'ils sont mineurs.
                </p> 
                <h5>Heures de battement.</h5>
                <p>
                - Les élèves dîneurs n'ayant pas cours en 5e ou en 6e heure ne peuvent quitter l'établissement et se rendent à
                l'étude.
                - Les élèves des 1e, 2e, 3e et 4e années, en cas d'absence d'un professeur ou pendant une heure de battement
                prévue à l'horaire, se rendent à l'étude.
                - Les élèves des 5e et 6e années qui ont reçu de leurs parents une autorisation de sortie, consignée au journal de
                classe, peuvent quitter l'établissement pendant les heures de battement. Dans le cas contraire, ils se rendent à
                l'étude.</p>
                <h5>Absences prévisibles.</h5>
                <p>
                En cas d’absence prévisible, la justification sera soumise au préalable à l’appréciation du Chef d’établissement ou
                de son délégué et confirmée, si possible, par un document officiel (par exemple: attestation de visite chez un
                médecin, à l’hôpital, ...)
                <p> Retards et absences injustifiés sont sanctionnés par une perte de points à la note de comportement et/ou une
                des sanctions prévues à l’article 8 appliquées de façon progressive. Une sanction peut être prise aussi dans le cadre
                du cours: travail supplémentaire, perte de points pour travail ou contrôle non fait,...</p>
                <h5>Cours d’éducation physique.</h5>
                <p>
                - La présence au cours est obligatoire. <br>
                - Les élèves dispensés temporairement se voient confier des tâches compatibles avec leur condition physique, soit
                dans le cadre du cours, soit à l'étude. L'évaluation de ces tâches contribue à la note du bulletin. <br>
                -Les élèves dispensés qui ont remis à leur professeur AVANT le 15/9 un certificat médical les couvrant jusqu'au
                30/6 assistent au cours ou, en cas d'impossibilité, restent à l'étude. Des tâches pourront leur être confiées. Ils n'ont
                pas de note d'éducation physique au bulletin. <br></p>
                <h5>Absence momentanée des parents.</h5>
                <p>
                Les parents qui s'absentent pour une période déterminée doivent communiquer à l'école AVANT LEUR
                DEPART le nom, l'adresse et le n° de téléphone de la personne à laquelle ils confient la responsabilité de leur
                enfant ainsi que les dates exactes de leur absence.</p>
                <h4>Art. 8 – Des sanctions disciplinaires.</h4>
                <p>- Tout acte, comportement ou abstention répréhensibles commis non seulement dans l'enceinte de l'école, mais
                aussi hors de l'établissement si les faits reprochés ont une incidence directe sur la bonne marche de l'établissement,
                peut être sanctionné. <br>
                - Les sanctions disciplinaires sont, dans l'ordre de gravité : le rappel à l'ordre (qui peut s’accompagner de tâches
                supplémentaires, objets d'une évaluation), la retenue, l'exclusion temporaire d'un cours ou de tous les cours
                d'un même enseignant, l'exclusion temporaire de tous les cours, l'exclusion définitive de l'établissement.
                4 <br>
                - La sanction est proportionnelle à la gravité des faits et à leurs antécédents éventuels. <br>
                - L’élève qui comptabilise 5 retenues est passible d’une journée d’exclusion de tous les cours. <br>
                - L'élève qui refuse une sanction est passible de la sanction suivante, dans l'ordre de gravité. <br>
                - Les tâches supplémentaires qui accompagnent la sanction peuvent être la réparation des torts causés à la
                victime, un travail d'intérêt général ou un travail pédagogique. <br>
                - Un recours contre les retenues et exclusions temporaires peut être introduit auprès du chef d’établissement; un
                recours contre l’exclusion définitive peut être introduit auprès du ministre. <br>
                Ces sanctions sont appliquées conformément aux articles 30 à 48 du Règlement d’ordre intérieur des
                établissements d’enseignement secondaire de la Communauté française (voir art. 1).</p>
                <h4>Art. 9 - Des documents scolaires et de la validation</h4>
                <p>Tout changement survenant dans la situation familiale ou administrative de l’élève ou de ses parents doit
                être communiqué immédiatement au secrétariat: déménagement, changement de numéro de téléphone
                (domicile ou travail), séparation, garde de l’enfant,... <br>
                L'élève doit toujours être en mesure de présenter son journal de classe au membre du personnel qui le réclame. <br>
                - Le journal de classe est signé par les parents de l’élève mineur au moins une fois par semaine ainsi qu'à côté de
                toute remarque ou communication éventuelle (licenciements, sanctions, points,...) <br>
                - Pour chaque heure de cours, il sera fait mention, des tâches à accomplir pour ce jour (devoirs, leçons, <br>
                préparations, ...) et, au minimum, la révision de la matière vue à la leçon précédente
                - L'élève absent met son journal de classe en ordre, dès son retour à l'école. <br>
                - En l'absence d'un professeur, les travaux et leçons seront reportés de leçon en leçon jusqu'à son retour. <br>
                - Le journal de classe constitue, comme les cahiers et les travaux écrits, un document officiel qui peut être
                demandé par la Direction générale de l’Enseignement obligatoire. <br>
                Par conséquent: <br>
                - le journal de classe et les cahiers en ordre doivent être conservés par l’élève pendant toute la durée de sa scolarité
                jusqu’à l’obtention du Certificat d’enseignement secondaire supérieur validé. <br>
                - les élèves qui ont terminé leur sixième s’engagent à fournir, dans un délai maximum de 48 heures, tout document
                demandé par la Direction générale de l’Enseignement obligatoire et à signaler tout changement d’adresse
                éventuel. <br>
                - Les élèves de sixième qui ne peuvent souscrire à cet engagement pour quelque motif que ce soit (départ à
                l’étranger, ...) doivent remettre à l’école, à la date prévue par les instructions de fin d’année, leurs journaux de
                classe de 4e, 5e et 6e et leurs cahiers de 5e et 6e, sous paquets ficelés et munis d’une étiquette indiquant leurs nom,
                prénom et classe.</p>
